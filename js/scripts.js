
$(document).ready(function() {
  $("button#black").click(function() {
    $("body").removeClass();
    $("body").addClass("black-background")
  });

  $("button#blue").click(function() {
    $("body").removeClass();
    $("body").addClass("blue-background")
  });

  $("button#white").click(function() {
    $("body").removeClass();

  });

});
